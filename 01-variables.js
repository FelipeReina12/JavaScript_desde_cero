// var ya no se usa en código moderno
var globalVar = "Soy una variable global";
console.log(globalVar);

// let es usada para variables que pueden cambiar
let valor = 10;
valor = 30;  // Reasignación de valor pero sin declarar de nuevo
console.log(valor);

// const es usada para valores que no cambian
const Pi = 3.1416;
console.log(Pi);
